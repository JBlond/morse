        '01' => 'А', '1000' => 'Б', '011' => 'В', '110' => 'Г', '100' => 'Д', '0' => 'Е',
        '0001' => 'Ж', '1100' => 'З', '00' => 'И', '0111' => 'Й', '101' => 'К', '0100' => 'Л',
